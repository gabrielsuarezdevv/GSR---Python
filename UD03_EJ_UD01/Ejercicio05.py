radio = float(input("Introduce el radio de la circunferencia: "))
longitudCircunferencia = 2 * 3.1416 * radio
areaCirculo = 3.1416 * radio ** 2
volumenEsfera = (4/3) * 3.1416 * radio ** 3
print("Resultados:")
print(f"Longitud de la circunferencia: {longitudCircunferencia}")
print(f"Área del círculo: {areaCirculo}")
print(f"Volumen de la esfera: {volumenEsfera}")
